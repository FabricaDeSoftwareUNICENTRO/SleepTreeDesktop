    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        if(jTextNome.getText().equals("")||jTextDesc.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Você não selecionou uma área ou não preencheu os campos 'Local' e 'Ano'!.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            ConectandoBanco conect = new ConectandoBanco();
            conect.execUp("Update area set nomeArea = '"+jTextNome.getText() + "-" + jTextDesc.getText()+"' where nomeArea = '"+this.nomeAreaModificada+"';");
            this.nomeAreaModificada = jTextNome.getText();
            
            conect.execUp("Update area set descArea = ' ' where nomeArea = '"+this.nomeAreaModificada+"';");

            conect.execUp("Update area set localArea = '"+jTextLocalidade.getText()+"' where nomeArea = '"+this.nomeAreaModificada+"';");

            conect.execUp("Update area set tipoArea = '"+jTextTipo.getText()+"' where nomeArea = '"+this.nomeAreaModificada+"';");

            
            
            conect.configuraComboBox(jComboBoxArea);
            JOptionPane.showMessageDialog(null,"Sua área foi alterada com sucesso.", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_jButtonModificarActionPerformed

