    private void jButtonSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelecionarActionPerformed
            try {
            ConectandoBanco conect = new ConectandoBanco();
            //Puxa dados do banco para o ComboBOx
            //conect.configuraComboBox(jComboBoxArea);
            
            ResultSet RS =null;
            
                String st = jComboBoxArea.getSelectedItem().toString();
                //String st = ob.toString();
            //conect.conecta();
            RS = conect.dadosArea(st, "nomearea");
            String aux1 = RS.getString("nomearea");
            String aux[] = aux1.split(Pattern.quote("-"));
            jTextNome.setText(aux[0]);
            this.nomeAreaModificada = RS.getString("nomearea");
            conect.desconecta();
            
            RS = conect.dadosArea(st, "descarea");
            jTextDesc.setText(aux[1]);
            conect.desconecta();
            
            RS = conect.dadosArea(st, "localarea");
            jTextLocalidade.setText((String) RS.getString("localarea"));
            conect.desconecta();
            
            RS = conect.dadosArea(st, "tipoarea");
            jTextTipo.setText((String) RS.getString("tipoarea"));
            conect.desconecta();
            //conect.configuraComboBox(jComboBoxArea);
            
        } catch (SQLException ex) {
            Logger.getLogger(VModificarArea.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonSelecionarActionPerformed

